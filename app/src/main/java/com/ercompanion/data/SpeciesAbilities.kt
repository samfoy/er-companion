    /**
     * Get ability ID for a species and ability slot
     * @param speciesId Pokemon species ID
     * @param slot 0=ability1, 1=ability2, 2=hidden ability
     * @return ability ID, or 0 if not found
     */
    fun getAbility(speciesId: Int, slot: Int): Int {
        val abilities = ABILITIES[speciesId] ?: return 0
        val abilityId = when (slot) {
            0 -> abilities.first
            1 -> abilities.second
            2 -> abilities.third
            else -> 0
        }
        // Fallback: if requested ability slot is 0, use ability1 instead
        return if (abilityId == 0 && slot > 0) abilities.first else abilityId
    }

    /**
     * Check if an ability slot is truly a hidden ability
     * Only slot 2 is hidden if there are two regular abilities (slot 1 != 0)
     * If slot 1 is 0 (NONE), then slot 2 is just the second regular ability
     */
    fun isHiddenAbility(speciesId: Int, slot: Int): Boolean {
        if (slot != 2) return false
        val abilities = ABILITIES[speciesId] ?: return false
        // Only hidden if ability2 exists (is not 0)
        return abilities.second != 0
    }
